//! # 音效模块
//!
//! 提供游戏音效管理功能，使用程序化生成的 WAV 音频。
//!
//! ## 设计特点
//!
//! - **无外部依赖**: 所有音效在运行时生成，无需音频文件
//! - **轻量级**: 使用简单的正弦波合成
//! - **可定制**: 可调整频率、时长、音量
//!
//! ## 音效类型
//!
//! | 音效 | 频率 | 时长 | 用途 |
//! |------|------|------|------|
//! | eat | 880Hz | 0.08s | 吃到普通食物 |
//! | trap | 330Hz | 0.15s | 吃到陷阱果实 |
//! | power | 660Hz | 0.2s | 吃到功能果实 |
//! | over | 220Hz | 0.3s | 游戏结束 |
//!
//! ## WAV 格式
//!
//! 生成的 WAV 文件格式：
//! - 采样率: 44100 Hz
//! - 位深度: 16-bit
//! - 声道: 单声道 (Mono)
//! - 编码: PCM

